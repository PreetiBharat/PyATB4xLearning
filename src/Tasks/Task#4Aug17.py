#  Write a Python program to calculate the area of a circle given its radius using the formula
#  ``` area=π×r^2``` ( Take pie as 3.14)

radius = int(input("Enter radius of the circle\n"))
pi = 3.1415
area = pi * (radius ** 2)
print(f"Area of the Circle-> {area}")

