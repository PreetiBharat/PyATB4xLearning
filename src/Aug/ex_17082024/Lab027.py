# Literals
# variable name = variable value

# Numeric literals
age = 65  # literal is of type int
is_preeti_married = True  # literal is of type bool

# Python uses decimal system and it has a base 10
age = 89  # 9

# Binary system: has a base 2
binary_number = 0b1010  # 10
print(binary_number)

# Octal --> base--> 8
o = 0o130  # -88
print(o)

# Hex -> base-> 16
d = 0x12c
print(d)

# String literals
# string - str
name = "Preeti's home"
print(name)

# multiline_string
multiline_str = """This is a multiline
string with more than one
line code."""
print(multiline_str)

unicode = u"\u00dcnic\u00f6de"
raw_str = r"raw \n string"
print(unicode)
print(raw_str)

# Complex Literal
complex_number = 1 + 7j

# Floating Point Literal
float_1 = 10.5
float_2 = 1.5e2
print(float_1, float_2)

# list
my_list = ["tea", "milk", "bread"]
print(my_list)

# tuple
my_tuple = ("Swara", "Bharat", "Chougule")
print(my_tuple)
