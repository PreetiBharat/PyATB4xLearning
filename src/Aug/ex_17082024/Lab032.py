# Not operators
is_preeti_married = True
print(not is_preeti_married)
print(is_preeti_married)

# Logical  operators: <,>,<=,>=
x = 10
y = 20
print(x > y)
print(x < y)
print(x >= y)

a = 10
b = 10
print(a >= b)
print(a == b)

# OR and AND Gate mechanism
f = False
t = True
print(f or t)
print(f and t)
