# x // y  # Quotient
# x % y   # Remainder
# x ** y   # power
# ==       # comparative

print(5 // 2)  # "//" this gives Quotient as output
print(5 % 2)  # "%" this gives Remainder as output

print(13 // 2)
print(13 % 2)

print(2 ** 3)  # ** is power operator
print(3 ** 2)

# == comparative operator(True, False)
print(2 == 2)
print(3 == 8)
